<?php

namespace Stellar\Vortex\Helpers\Typography\Enum;

enum Typography: string
{
    case Ampersand = '&';
    case Backslash = '\\';
    case CommaWhiteSpace = ', ';
    case Dot = '.';
    case EndParentheses = ')';
    case EmptyString = '';
    case OpenParentheses = '(';
    case Semicolon = ';';
    case WhiteSpace = ' ';
    case Slash = '/';
    case SingleQuote = "'";
    case DoubleQuote = '"';
    case EqualOperator = '=';
    case Underline = '_';
    case Percentage = '%';
}
